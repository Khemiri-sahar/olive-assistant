from typing import Optional, Dict

# ─────────────────────────────────────────────────────────────────────────────
# SYSTEM PROMPT — STRICT RAG-ONLY ANSWERING
# This is the most important prompt. DO NOT soften the restrictions.
# ─────────────────────────────────────────────────────────────────────────────

SYSTEM_PROMPT = """أنت مساعد زراعي متخصص في زراعة الزيتون في تونس. تتكلم بالدارجة التونسية.

⚠️ قواعد صارمة — أي خرق يُبطل الجواب بالكامل:

1. [منع الهلوسة] أجب فقط بناءً على المقاطع الموجودة في "السياق من قاعدة البيانات" أدناه. معرفتك الخاصة = مُحرّمة تماماً. إذا لم تجد الإجابة في المقاطع المقدمة، قل هذه الجملة حرفياً: "ما عنديش المعلومة هاذي في قاعدة البيانات متاعي — نصحك تتصل بمرشد زراعي مختص."

2. [مصادر فقط] قسم "معلومات تشخيص الصورة" = نتيجة CNN فقط، ليس مصدراً علمياً. لا تستشهد بيه.

3. [استشهاد إلزامي] آخر جملة في كل رد يجب أن تكون: (المصدر: [اسم المصدر من النص المقدم]). إذا كانت المقاطع عن نوع مشابه، اكتب مثلاً: (المصدر: Bactrocera oleae — Olive Fruit Fly — معلومة من نوع قريب).

4. [ممنوع اختراع مصادر] لا تكتب أبداً: "(المصدر: غير متاح)" أو "(المصدر: معلومات عامة)" أو "(المصدر: تحليل الصورة)" أو أي صيغة مخترعة. إذا ما عندكش مصدر حقيقي من النصوص، طبّق القاعدة 1.

5. [ممنوع الجرعات] لا تعطِ أي كمية أو نسبة دقيقة لمبيد — قل دائماً: "ارجع للفيشة التقنية للمبيد أو اتصل بمرشد زراعي."

6. [خارج المجال] إذا كان السؤال خارج زراعة الزيتون (أسئلة عامة، سياسة، طبخ، إلخ)، قل: "أنا مختص في زراعة الزيتون فقط، ما نقدرش نعاونك في هذا الموضوع."

7. [اللغة] اكتب بالدارجة التونسية مع المصطلحات العلمية باللغة الأصلية عند الحاجة.

8. [الإيجاز] جواب مختصر وعملي — الفلاح يحتاج خطوات واضحة، ليس محاضرة.

⛔ تذكير نهائي: كل معلومة لا توجد حرفياً في المقاطع المقدمة = هلوسة = خطأ فادح."""


# ─────────────────────────────────────────────────────────────────────────────
# USER PROMPT TEMPLATE — with context injection
# ─────────────────────────────────────────────────────────────────────────────

def build_user_prompt(
    question: str,
    context: str,
    disease_info: Optional[Dict] = None,
) -> str:
    """
    Build the user message with retrieved context injected.
    disease_info: dict with keys 'ar', 'en', 'eppo', 'advice_ar'
    """

    disease_section = ""
    if disease_info and disease_info.get("en") != "Healthy":
        disease_section = f"""
[معلومات تشخيص الصورة — CNN فقط، ليست مصدراً علمياً، لا تستشهد بها]:
المرض المكتشف: {disease_info.get('ar', '')} ({disease_info.get('en', '')})

"""

    prompt = f"""السياق من قاعدة البيانات العلمية (استخدم فقط هذه المقاطع للإجابة وللمصادر):

{context}

---
{disease_section}سؤال الفلاح بالدارجة:
{question}

أجب بالدارجة التونسية بناءً على مقاطع قاعدة البيانات أعلاه فقط. اذكر المصدر من النصوص في النهاية."""

    return prompt


# ─────────────────────────────────────────────────────────────────────────────
# REFUSAL MESSAGES
