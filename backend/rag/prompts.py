REFUSAL_LOW_RELEVANCE = (
    "آسف، ما لقيتش معلومة كافية في قاعدة البيانات متاعي باش نجاوبك على هذا السؤال. "
    "نصحك تتصل بمرشد زراعي مختص — هو يقدر يعاونك أكثر مني."
)

REFUSAL_OUT_OF_DOMAIN = (
    "آسف، أنا مختص فقط في زراعة الزيتون وأمراضه. "
    "هذا السؤال خارج تخصصي وما نقدرش نجاوبك فيه."
)

REFUSAL_DOSAGE = (
    "ما نقدرش نعطيك الكمية أو الجرعة الدقيقة للمبيد — هذا خطر على صحتك وصحة الغير ومسؤولية قانونية. "
    "ارجع للفيشة التقنية للمبيد أو اتصل بمرشد زراعي مختص باش يحدد الجرعة الصحيحة."
)

REFUSAL_MESSAGES = {
    "out_of_domain": REFUSAL_OUT_OF_DOMAIN,
    "dosage":        REFUSAL_DOSAGE,
    "low_relevance": REFUSAL_LOW_RELEVANCE,
}

def get_refusal_message(reason: str) -> str:
    return REFUSAL_MESSAGES.get(reason, REFUSAL_LOW_RELEVANCE)